    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String username = usernameIn.getText();
        String password = passwordIn.getText();
        String firstname = firstnameIn.getText();
        String lastname = lastnameIn.getText();
        String mobilePhoneNumber = mobileNumberIn.getText();
        String addressline = addresslineIn.getText();
        String age = ageIn.getText();
        String gender = genderIn.getText();
        
         try{
            BufferedWriter out = new BufferedWriter(new FileWriter("./checks\\approvedDoctors.txt", true));
            out.newLine();
            out.newLine();
            out.write(username);
            out.newLine();
            out.write(password);
            out.newLine();
            out.write(firstname);
            out.newLine();
            out.write(lastname);
            out.newLine();
            out.write(mobilePhoneNumber);
            out.newLine();
            out.write(addressline);
            out.newLine();
            out.write(age);
            out.newLine();
            out.write(gender);  
            out.close();
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

